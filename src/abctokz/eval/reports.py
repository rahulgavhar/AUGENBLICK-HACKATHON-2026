"""Report generation: markdown and JSON summaries."""

from __future__ import annotations

from abctokz.types import BenchmarkResult


def results_to_markdown(results: list[BenchmarkResult], title: str = "Benchmark Results") -> str:
    """Format benchmark results as a Markdown table.

    Args:
        results: List of benchmark results.
        title: Report title.

    Returns:
        Markdown string.
    """
    lines = [f"# {title}", ""]

    if not results:
        lines.append("*No results.*")
        return "\n".join(lines)

    header = (
        "| Tokenizer | Lang | Sentences | Throughput (sps) | "
        "Mean tokens/sent | Fertility | UNK rate | Round-trip% | Seq-len ratio |"
    )
    separator = "|-|-|-|-|-|-|-|-|-|"
    lines += [header, separator]

    for r in results:
        row = (
            f"| {r.tokenizer_name} "
            f"| {r.language} "
            f"| {r.n_sentences} "
            f"| {r.throughput_sps:.1f} "
            f"| {r.mean_tokens_per_sentence:.2f} "
            f"| {r.fertility:.3f} "
            f"| {r.unk_rate:.4f} "
            f"| {r.round_trip_success_rate * 100:.1f}% "
            f"| {r.normalized_seq_length_ratio:.3f} |"
        )
        lines.append(row)

    lines += ["", f"*Generated by abctokz benchmark runner.*"]
    return "\n".join(lines)
